// Task 5: Square Function
function square(number) {
    return number * number;
  }
  
  let squaredNumber = square(9);
  console.log("Square of 9:", squaredNumber); // Output: Square of 9: 81
  
  let anotherSquaredNumber = square(2);
  console.log("Square of 2:", anotherSquaredNumber); // Output: Square of 2: 4